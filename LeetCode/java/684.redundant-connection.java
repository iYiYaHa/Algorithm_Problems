// @lc code=start
class Solution {
    class UnionFind {
        private int[] parent;
        private int[] size;

        UnionFind(int n) {
            parent = new int[n];
            size = new int[n];
            for (int i = 0; i < n; ++i) {
                parent[i] = i;
            }
        }

        public int getRoot(int i) {
            while (i != parent[i]) {
                parent[i] = parent[parent[i]];
                i = parent[i];
            }
            return i;
        }

        public boolean isConnected(int i, int j) {
            return getRoot(i) == getRoot(j);
        }

        public void union(int i, int j) {
            int rootI = getRoot(i);
            int rootJ = getRoot(j);
            if (rootI == rootJ)
                return;
            int sizeI = size[rootI];
            int sizeJ = size[rootJ];
            if (sizeI <= sizeJ) {
                parent[rootI] = rootJ;
                size[rootJ] += size[rootI];
            } else {
                parent[rootJ] = rootI;
                size[rootI] += size[rootJ];
            }
        }
    }

    public int[] findRedundantConnection(int[][] edges) {
        if (edges == null || edges.length <= 0)
            return new int[] {};
        int n = edges.length;
        UnionFind uf = new UnionFind(n + 1);
        for (int[] edge : edges) {
            int i = edge[0], j = edge[1];
            if (uf.isConnected(i, j)) {
                return edge;
            } else {
                uf.union(i, j);
            }
        }
        return new int[] {};
    }
}
